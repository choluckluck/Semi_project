package hyerin.product.model;

public class ProductImageVO {
	private String product_image_code;
	private String fk_prod_code;
	private String product_image_file;
	
	
	public String getProduct_image_code() {
		return product_image_code;
	}
	public void setProduct_image_code(String product_image_code) {
		this.product_image_code = product_image_code;
	}
	public String getFk_prod_code() {
		return fk_prod_code;
	}
	public void setFk_prod_code(String fk_prod_code) {
		this.fk_prod_code = fk_prod_code;
	}
	public String getProduct_image_file() {
		return product_image_file;
	}
	public void setProduct_image_file(String product_image_file) {
		this.product_image_file = product_image_file;
	}
	
	

}
